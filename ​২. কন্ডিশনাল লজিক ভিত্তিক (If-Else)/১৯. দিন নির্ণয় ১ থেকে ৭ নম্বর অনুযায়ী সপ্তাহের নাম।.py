# ১৯. দিন নির্ণয়: ১ থেকে ৭ নম্বর অনুযায়ী সপ্তাহের নাম।
day = int(input("Enter the day number: "))
if day == 1:
    print("Saturday")
elif day == 2:
    print("Sunday")
elif day == 3:
    print("Monday")
elif day == 4:
    print("Tuesday")
elif day == 5:
    print("Wednesday")
elif day == 6:
    print("Thursday")
elif day == 7:
    print("Friday")
else:
    print("Invalid day number")