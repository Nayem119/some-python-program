#  ডিজিটাল ডায়রি ও পাসওয়ার্ড লকার (Password Locker)
# মূল ধারণা: সঠিক পাসওয়ার্ড না দেওয়া পর্যন্ত while loop দিয়ে সিকিউরিটি পিন চাইবে। match-case ব্যবহার করে পাসওয়ারড দেখা, পরিবর্তন করা বা লগআউট করার অপশন দেওয়া হবে।
a="1234"  # ডিফল্ট পাসওয়ার্ড
while True:
    pin = input("পাসওয়ার্ড দিন: ")
    if pin == a:
        print("সফলভাবে লগইন হয়েছে।")
        print("1. পাসওয়ার্ড দেখা")
        print("2. পাসওয়ার্ড পরিবর্তন করা")
        print("3. লগআউট")
        choice = input("আপনার পছন্দ করুন (1/2/3): ")
        
        match choice:
            case "1":
                print(f"আপনার পাসওয়ার্ড: {a}")
            case "2":
                new_password = input("নতুন পাসওয়ার্ড দিন: ")
                a = new_password
                print("পাসওয়ার্ড সফলভাবে পরিবর্তন হয়েছে।")
            case "3":
                print("লগআউট করা হয়েছে।")
                break
            case _:
                print("অবৈধ অপশন। আবার চেষ্টা করুন।")
    else:
        print("ভুল পাসওয়ার্ড! আবার চেষ্টা করুন।")
        